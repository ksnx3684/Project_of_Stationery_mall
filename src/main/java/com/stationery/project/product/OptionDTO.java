package com.stationery.project.product;

public class OptionDTO {

	private Integer optionNum;
	private Integer productNum;
	private String optionContents;
	private Integer optionStock;
	
	
	public OptionDTO() {
	
		
	}
	
	public Integer getOptionNum() {
		return optionNum;
	}
	public void setOptionNum(Integer optionNum) {
		this.optionNum = optionNum;
	}
	public Integer getProductNum() {
		return productNum;
	}
	public void setProductNum(Integer productNum) {
		this.productNum = productNum;
	}
	public String getOptionContents() {
		return optionContents;
	}
	public void setOptionContents(String optionContents) {
		this.optionContents = optionContents;
	}
	public Integer getOptionStock() {
		return optionStock;
	}
	public void setOptionStock(Integer optionStock) {
		this.optionStock = optionStock;
	}
	
	
	
}


